const toboggan = (posRight = 3, posBottom = 1, entry = input) => {
  let tree = 0
  let currentValue = 0
  let lengthEntry = entry[0].length
  for (let i = 0; i < entry.length; i++) {
    if (i % posBottom !== 0) {
      continue
    }
    if (entry[i].split('')[currentValue] === '#') {
      tree++
    }
    currentValue = (currentValue + posRight) % lengthEntry
  }
  return tree
}

